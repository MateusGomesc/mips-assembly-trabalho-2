li $t0, 1               # i = 1 (coluna)
move $t1, $s3           # endereço da primeira célula da linha

LoopColuna:
slti $t5, $t0, 31
beq $t5, $zero, ProxLinha

move $t7, $t1           # endereço da célula atual

# ------------------------------------------------
# Conta vizinhos vivos
li $t9, 0
lb $t8,  1($t7)
add $t9, $t9, $t8
lb $t8, -1($t7)
add $t9, $t9, $t8

sub $t2, $t7, $s2       # linha de cima
lb $t8,  0($t2)
add $t9, $t9, $t8
lb $t8,  1($t2)
add $t9, $t9, $t8
lb $t8, -1($t2)
add $t9, $t9, $t8

add $t2, $t7, $s2       # linha de baixo
lb $t8,  0($t2)
add $t9, $t9, $t8
lb $t8,  1($t2)
add $t9, $t9, $t8
lb $t8, -1($t2)
add $t9, $t9, $t8

# ------------------------------------------------
# Regras do Jogo da Vida
lb $t3, 0($t7)
beq $t3, $zero, CelulaMorta

# Célula viva: sobrevive com 2 ou 3 vizinhos
li $t6, 2
beq $t9, $t6, VivaProx
li $t6, 3
beq $t9, $t6, VivaProx
j Morre

CelulaMorta:
# Célula morta: nasce com exatamente 3 vizinhos
li $t6, 3
beq $t9, $t6, Nasce

sb $zero, 0($s1)        # célula permanece morta — grava 0
j Continua

Nasce:
li $t8, 1
sb $t8, 0($s1)
j Continua

VivaProx:
li $t8, 1
sb $t8, 0($s1)
j Continua

Morre:
sb $zero, 0($s1)

# ------------------------------------------------
Continua:
addi $t1, $t1, 1        # avança coluna na origem
addi $s1, $s1, 1        # avança coluna no destino
addi $t0, $t0, 1
j LoopColuna

# ------------------------------------------------
ProxLinha:
add $s3, $s3, $s2       # próxima linha na origem (+22 bytes)
addi $s1, $s1, 2        # pula bordas direita e esquerda no destino
addi $t4, $t4, 1
j LoopLinha

# ------------------------------------------------
FimAtualiza:
# Troca ponteiros: $s0 passa a apontar para o destino recém-calculado
move $t0, $s0
move $s0, $s4           # $s0 = matriz destino (agora é a atual)
move $s1, $t0           # $s1 = matriz origem (agora disponível para reutilizar)
jr $ra
